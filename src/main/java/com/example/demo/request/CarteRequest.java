package com.example.demo.request;

public class CarteRequest {
	
	public String page;
	
	public String action;
	
	public String methode;
	
	public String getDataAdresse;
	
	public String image;
	
	public String titre;
	
	public String[] getData;
	
	public String[] contenu;
	
	public String NvPage;

	public String getNvPage() {
		return NvPage;
	}

	public void setNvPage(String nvPage) {
		NvPage = nvPage;
	}

	public String getPage() {
		return page;
	}

	public void setPage(String page) {
		this.page = page;
	}

	public String getAction() {
		return action;
	}

	public void setAction(String action) {
		this.action = action;
	}

	public String getMethode() {
		return methode;
	}

	public void setMethode(String methode) {
		this.methode = methode;
	}

	public String getGetDataAdresse() {
		return getDataAdresse;
	}

	public void setGetDataAdresse(String getDataAdresse) {
		this.getDataAdresse = getDataAdresse;
	}

	public String getImage() {
		return image;
	}

	public void setImage(String image) {
		this.image = image;
	}

	public String getTitre() {
		return titre;
	}

	public void setTitre(String titre) {
		this.titre = titre;
	}

	public String[] getGetData() {
		return getData;
	}

	public void setGetData(String[] getData) {
		this.getData = getData;
	}

	public String[] getContenu() {
		return contenu;
	}

	public void setContenu(String[] contenu) {
		this.contenu = contenu;
	}
	
	
	
}
